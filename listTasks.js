
function largest(list){
let largest = list[0];
    for(let a = 0; a < list.length; a++){
        if(list[a] > largest){
            largest = list[a];
        }
    }
    return largest;
}

function reverse(list){
let array = [];
    for(let a = 0; a < list.length; a++){
        array.push(list[a]);
    }
    return array;
}

